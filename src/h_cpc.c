/**
    @brief  Преобразование строки из кодовой таблицы IS08859 Part 5 (Latin/Cyrillic) в Win1251.
    @param  p_buf_src - исходные данные.
    @param  p_len     - длина исходных данных.
    @param  p_buf_dest   - буфер, куда помещается результат (может совпадать с исходным).
    @retval указатель на p_buf_dest.
*/
unsigned char * h_cp_Iso8859_05_to_1251 (unsigned char *p_buf_src, unsigned char p_len, unsigned char *p_buf_dest)
{
    unsigned char i;

    for (i = 0; i < p_len; i++)
    {
        if (p_buf_src[i] >= 0xB0)
        {
            p_buf_dest[i] = p_buf_src[i] + 0x10;
        }
        else if (p_buf_src[i] == 0xA1) // Ё
        {
            p_buf_dest[i] = 0xA8;
        }
        else if (p_buf_src[i] == 0xF1) // ё
        {
            p_buf_dest[i] = 0xB8;
        }
        else
        {
            // latin symbols (no changes)
            p_buf_dest[i] = p_buf_src[i];
        }
    }

    return p_buf_dest;
}
/**
    @brief  Преобразование строки из кодовой таблицы Win1251 to utf8.
    @param[in]  p_str_src - исходные данные.
    @param[out]  p_str_dst   - буфер, куда помещается результат (может совпадать с исходным).
*/
void h_cp1251_to_utf8(const char *p_str_src, char *p_str_dst)
{
    while (*p_str_src)
        if (*p_str_src & 0x80)
        {
            int v = g_cp1251_to_utf8[(int)(0x7f & *p_str_src++)];
            if (!v)
                continue;
            *p_str_dst++ = (char)v;
            *p_str_dst++ = (char)(v >> 8);
            if (v >>= 16)
                *p_str_dst++ = (char)v;
        }
        else
            *p_str_dst++ = *p_str_src++;
    *p_str_dst = 0;
}
/**
    @brief  Преобразование строки из кодовой таблицы utf8 to Win1251.
    @param[in]  p_str_src   исходные данные.
    @param[out] p_str_dst   буфер, куда помещается результат (может совпадать с исходным).
    @param[in] n    входная длина.
    @return 0 успешно , -1 не успешно.
*/
int h_utf8_to_cp1251(const char* p_str_src, char* p_str_dst, unsigned int n)
{
	int i = 0;
	int j = 0;
	for(; i < (int)n && p_str_src[i] != 0; ++i) {
		char prefix = p_str_src[i];
		char suffix = p_str_src[i + 1];
		if ((prefix & 0x80) == 0) {
            p_str_dst[j] = (char)prefix;
			++j;
		} else if ((~prefix) & 0x20) {
			int first5bit = prefix & 0x1F;
			first5bit <<= 6;
			int sec6bit = suffix & 0x3F;
			int unicode_char = first5bit + sec6bit;

			if ( unicode_char >= 0x410 && unicode_char <= 0x44F ) {
                p_str_dst[j] = (char)(unicode_char - 0x350);
			} else if (unicode_char >= 0x80 && unicode_char <= 0xFF) {
                p_str_dst[j] = (char)(unicode_char);
			} else if (unicode_char >= 0x402 && unicode_char <= 0x403) {
                p_str_dst[j] = (char)(unicode_char - 0x382);
			} else {
				int count = sizeof(g_letters) / sizeof(Letter);
				for (int k = 0; k < count; ++k) {
					if (unicode_char == g_letters[k].unicode) {
                        p_str_dst[j] = g_letters[k].win1251;
						goto NEXT_LETTER;
					}
				}
				// can't convert this char
				return -1;
			}
NEXT_LETTER:
			++i;
			++j;
		} else {
			// can't convert this chars
			return -1;
		}
	}
    p_str_dst[j] = 0;
	return 0;
}
