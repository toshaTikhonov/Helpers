/**
    @brief Преобразование символа заданного ch в кодировке 1251 
    в кодировку 866
    @param   ch   -   символ в колировке 1251
    @retval  символ в кодировке 866
*/
unsigned char h_cp_1251_to_866 (unsigned char ch)
{
    return g_cp1251_to_866 [ch];
}
